var current_que = 0;
var total_question = q_and_a.length;

function updateQuestion() {
  $("#answers button").removeClass("correct_answer");
  $("#answers button").removeClass("wrong_answer");
  $("#next").fadeOut(50);

  current_question = q_and_a[current_que];
  $("#question").html(current_question.question);
  for (var i = 0; i < 4; i++) {
    $("#choice" + i).html(current_question.answers[i].text);
    $("#answers #btn" + i).attr(
      "data-correct",
      current_question.answers[i].isCorrect
    );
  }
  $("#progress").html(
    "Question " + (current_que + 1) + " of " + total_question
  );
  current_que += 1;
}

$(document).ready(function() {
  updateQuestion();

  $("#answers button").on("click", function() {
    var isTrue = $(this).attr("data-correct") == "true";
    if (isTrue) {
      $(this).addClass("correct_answer");
    } else {
      $(this).addClass("wrong_answer");
    }
    if (current_que < total_question) {
      $("#next").fadeIn(50);
    }
  });
});
